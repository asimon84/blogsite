        'category',
        'thumb',
        'image',
        'video',
        'content',
    ];

    /**
     * The attributes that should be hidden for serialization.
     *
     * @var list<string>
     */
    protected $hidden = [

    ];

    /**
     * Get the attributes that should be cast.
     *
     * @return array<string, string>
     */
    protected function casts(): array
    {
        return [
            'title' => 'string',
            'author' => 'string',
            'category' => 'string',
            'thumb' => 'string',
            'image' => 'string',
            'video' => 'string',
            'content' => 'string',
        ];
